<span style="font-weight: bold;">xvr2</span> is or intends to be a
cross platform C++ library, why it is under the GPL? because the only
way to achieve our goals is using all the help we can get from the
community, and is also fair to give this effort back to them.<br>
There 6 target platforms we have in mind for deploying this library:<br>
<ul>
  <li>Linux</li>
  <li>SGI/IRIX</li>
  <li>*BSD</li>
  <li>MacOSX</li>
  <li>SUN/Solaris</li>
  <li>IBM/AIX</li>
  <li>HP-UX</li>
</ul>
Right now we are working only on Linux because we had no access, of course, on
our free time to any of the above environments, so please if anyone
wishes to donate a box of each of the above it will be greatly
appreciated. I had access in the past to some SUN boxes so a little
porting work has been done, probably in a few months I will have access
to some AIX boxes but for the other platforms I really have some
difficulties.<br>
