<span style="font-weight: bold;"><br>
History</span><br>
Some friends and I use to work for a system integration company, this
company had an experimental software development department, someday
they told us to build somekind of scheduling daemon, but we prefer to
play and use the opportunity to experiment a little, by that time we
created a Linux/Solaris cross platform, but because of an NDA we signed
the code remained with them, xvr2 is a complete new implementation of
that library in fact is better, most structured, better planned and
much, much more reliable.<br>
