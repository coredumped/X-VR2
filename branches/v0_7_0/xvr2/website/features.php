<big style="font-weight: bold;"><big><big>Features</big></big></big><br>
Actually there are many features and goals to be accomplished, maybe
more features might come in the future, this is still alpha software,
but currently we have this features:<br>
<ul>
  <li>Thread safe methods on all classes.</li>
  <li>Cross platform thread implementation using POSIX Threads, GNU Pth
or SDL.</li>
  <li>Dynamic library object loading.</li>
  <li>Relational database access through the use of drivers</li>
  <li>A powerfull string implementation</li>
  <li>Network access through our Socket classes</li>
  <li>Exception handling and dispatching</li>
  <li>Runtime checking and exception delivery for some wrapped Unix
system calls</li>
  <li>Documentation through doxygen usage.</li>
  <li>Multiple mutual exclusion handling features such as mutexes,
condition variables and semaphores.</li>
  <li>Host system feature detection.</li>
  <li>Did I say that this whole thing is cross-plaform??<br>
  </li>
</ul>
